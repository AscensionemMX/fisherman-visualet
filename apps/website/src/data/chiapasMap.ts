export function projectChiapasToUnit([lon, lat]: ChiapasCoord) {
  return {
    x: (lon - CHIAPAS_BOUNDS.minLon) / (CHIAPAS_BOUNDS.maxLon - CHIAPAS_BOUNDS.minLon),
    y: (CHIAPAS_BOUNDS.maxLat - lat) / (CHIAPAS_BOUNDS.maxLat - CHIAPAS_BOUNDS.minLat),
  };
}

export function projectChiapasToIntro(coord: ChiapasCoord) {
  const point = projectChiapasToUnit(coord);

  return {
    x: 120 + point.x * 860,
    y: 70 + point.y * 500,
  };
}
